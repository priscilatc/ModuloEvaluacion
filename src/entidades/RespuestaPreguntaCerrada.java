package entidades;

public class RespuestaPreguntaCerrada {

	private int idRespuesta_preguntaCerrada;
	private int idPreguntaCerrada;
	private int idEvaluacion;
	private int estado;
	
	public int getIdRespuesta_preguntaCerrada() {
		return idRespuesta_preguntaCerrada;
	}
	public void setIdRespuesta_preguntaCerrada(int idRespuesta_preguntaCerrada) {
		this.idRespuesta_preguntaCerrada = idRespuesta_preguntaCerrada;
	}
	public int getIdPreguntaCerrada() {
		return idPreguntaCerrada;
	}
	public void setIdPreguntaCerrada(int idPreguntaCerrada) {
		this.idPreguntaCerrada = idPreguntaCerrada;
	}
	public int getIdEvaluacion() {
		return idEvaluacion;
	}
	public void setIdEvaluacion(int idEvaluacion) {
		this.idEvaluacion = idEvaluacion;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
}
